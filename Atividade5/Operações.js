var num1 = prompt("Digite o primeiro valor:");
var num2 = prompt("Digite o segundo valor:");
    
soma = parseFloat(num1) + parseFloat(num2);

subtracao = parseFloat(num1) - parseFloat(num2);

produto = parseFloat(num1) * parseFloat(num2);

divisao = parseFloat(num1) / parseFloat(num2);

resto = parseFloat(num1) % parseFloat(num2);
        
alert (" A soma dos valores informados é: "+soma+"\n A subtração do primeiro pelo segundo é: "+subtracao+"\n O produto dos dois números é: "+produto+"\n A divisão entre eles é: "+divisao+"\n O resto da divisão é: "+resto);


